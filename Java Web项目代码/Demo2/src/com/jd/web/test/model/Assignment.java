package com.jd.web.test.model;

public class Assignment {
	
	private int id;                 //作者id
	
	private String title;           //作业标题
	
	private String content;         //作业内容
	
	private String people;          //作业作者
	
	private String createTime;      //上传时间
	
	private String updateTime;      //修改时间
	
	private String delTime;         //删除时间
	
	private String delUser;         //删除人员
	
	private String delFlage;        //删除状态
	
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getPeople() {
		return people;
	}

	public void setPeople(String people) {
		this.people = people;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getDelTime() {
		return delTime;
	}

	public void setDelTime(String delTime) {
		this.delTime = delTime;
	}

	public String getDelUser() {
		return delUser;
	}

	public void setDelUser(String delUser) {
		this.delUser = delUser;
	}

	public String getDelFlage() {
		return delFlage;
	}

	public void setDelFlage(String delFlage) {
		this.delFlage = delFlage;
	}
	
	
	
	
	
	
	
	

}
